// 12.某班考试成绩如下，利用二维数组存储这些数据，统计出全班的平均成绩
//   第一组： 89，66，54
//   第二组： 77，93，68
//   第三组： 55,   45,  88
//   第四组： 71,   76,  75

// 步骤：
var score = [[89,66,54],[77,93,68],[55,45,88],[71,76,75]]
// 声明一个总和
var sum = 0
// 声明一计数器
var count = 0
// 遍历所有元素
for (var i=0; i<score.length; i++)
{
    for (var j=0; j<score[i].length; j++)
    {
        // 加入总和
        sum += score[i][j]
        // 计数
        count++
    }
}
// 输出总和/总元素个数 = 平均值
console.log('平均值为'+sum/count);
