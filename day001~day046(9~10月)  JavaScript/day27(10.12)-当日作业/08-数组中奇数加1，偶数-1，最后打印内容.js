//  需求： 8.已知arr数组内容[1, 3, 5, 8, 10, 11, 16]，编码实现：数组中所有奇数加1，偶数-1,最后打印数组内容

//  分析：先用循环遍历出所有元素，然后将数组中奇数找出加一，并将偶数找出-1，最后打印数组内容

//  步骤：
var arr = [1, 3, 5, 8, 10, 11, 16]
for (var i=0; i<arr.length; i++)
{
    if (arr[i]%2!=0)
    {
        arr[i] = arr[i] + 1
    }
    else 
    {
        arr[i] = arr[i] - 1
    }
}
console.log(arr);
