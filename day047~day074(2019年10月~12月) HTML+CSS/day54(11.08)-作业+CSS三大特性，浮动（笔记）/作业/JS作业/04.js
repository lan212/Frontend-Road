// 4、求出1-1/4+1/3-1/16…..-1/100*100的和。

// 步骤：
// 准备一个变量作为和
let sum = 0;
// 循环到1-100
for (let i = 1; i <= 100; i++) {
    // 判断i的值是奇数还是偶数
    if (i % 2 == 0) {
        // 是偶数，加上-1/偶数的平方
        sum = sum - 1 / (i * i);
    }
    else {
        // 是奇数，加上 1/奇数
        sum += 1 / i;
    }
}
// 输出和
console.log(sum);
